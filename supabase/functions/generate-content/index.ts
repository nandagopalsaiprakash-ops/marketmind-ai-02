import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { contentType, topic, tone } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const typePrompts: Record<string, string> = {
      instagram: "Create an Instagram post with: Hook, Caption (with emojis), Call-to-Action, and 15-20 relevant Hashtags.",
      linkedin: "Create a LinkedIn post with: Attention-grabbing hook, Professional body with insights, Call-to-Action. Use line breaks for readability.",
      blog: "Create a blog outline with: Title, Meta description, H2/H3 structure, Key points per section, Target keywords, Internal linking suggestions.",
      adcopy: "Create ad copy with: Headline (max 30 chars), Primary Text, Description, Call-to-Action button text. Provide 3 variations.",
      email: "Create an email campaign with: Subject line (3 options), Preview text, Email body with personalization tokens, CTA button text, P.S. line.",
    };

    const systemPrompt = `You are an expert marketing content creator. Generate high-quality, platform-specific marketing content. Use markdown formatting. Be specific and actionable. Tone: ${tone}.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `${typePrompts[contentType] || "Create marketing content."}\n\nTopic: ${topic}` },
        ],
      }),
    });

    if (!response.ok) {
      const status = response.status;
      if (status === 429) return new Response(JSON.stringify({ error: "Rate limited. Please try again." }), { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } });
      if (status === 402) return new Response(JSON.stringify({ error: "Credits exhausted." }), { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } });
      throw new Error(`AI gateway error: ${status}`);
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content || "";

    return new Response(JSON.stringify({ content }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("content error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
